/**
 * Utilities for X-definitions executable from command line.
 * <H2><b>CheckXDef</b></H2>
 * Checks the source X-definition or DefPool
 * (see {@link org.xdef.util.CheckXdef#main(String[])}).
 * <H2><b>DTDToXdef</b></H2>
 * Create a X-definition from a DTD.
 * (see {@link org.xdef.util.DTDToXdef#main(String[])}).
 * <H2><b>GenDTD</b></H2>
 * Generates DTD according to given X-definition
 * (see {@link org.xdef.util.GenDTD#main(String[])}).
 * <i>Note that not all features from X-definitions can be converted to DTD
 * so this tool is only to help to design the DTD.</i>
 * <H2><b>GenCollection</b></H2>
 * Create XML document composed from set of X-definitions and/or collections.
 * (see {@link org.xdef.util.GenCollection#main(String[])}).
 * <H2><b>GenXdef</b></H2>
 * Create X-definition from XML document.
 * (see {@link org.xdef.util.GenXDefinition#main(String[])}).
 * <H2><b>PrettyXdef</b></H2>
 * Createtes the formatted X-definition source nice and more readable.
 * (see {@link org.xdef.util.PrettyXdef#main(String[])}).
 * <H2><b>XCompose</b></H2>
 * Composes the new XML from given source according to given X-definition.
 * (see {@link org.xdef.util.XCompose#main(String[])}).
 * <H2><b>XdefToXsd</b></H2>
 * Create a XML schema document created from a set of X-definitions and/or
 * collections (see {@link org.xdef.util.XdefToXsd#main(String[])}).
 * <H2><b>XsdToXdef</b></H2>
 * Create a X-definition from a XML schema
 * (see {@link org.xdef.util.XsdToXdef#main(String[])}).
 * <H2><b>XValidate</b></H2>
 * Validates and processes the XML data according to given X-definition
 * (see {@link org.xdef.util.XValidate#main(String[])}).
 * <H2><b>XDChecker</b></H2>
 * Enables to check a text value with a validation method.
 * (see {@link org.xdef.util.XDChecker}).
 *
 */
package org.xdef.util;